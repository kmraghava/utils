#define DOT_JOIN_ARGS9(_1, _2, _3, _4, _5, _6, _7, _8, _9)        _1._2._3._4._5._6._7._8._9
#define DOT_JOIN_ARGS10(_1, _2, _3, _4, _5, _6, _7, _8, _9, _10)  _1._2._3._4._5._6._7._8._9._10

#define DOT_JOIN_ARGS_IMPL2(N, ...)  DOT_JOIN_ARGS ##N(__VA_ARGS__)
#define DOT_JOIN_ARGS_IMPL(N, ...)  DOT_JOIN_ARGS_IMPL2(N, __VA_ARGS__)


/*****************************************************************************
 * Global Defines
 *****************************************************************************/
#define STRINGIZE(x)  STRINGIZE_IMPL(x)

#define CONCATENATE(x, y)  CONCATENATE_IMPL(x, y)

